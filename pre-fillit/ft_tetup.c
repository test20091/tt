#include "fillit.h"

void		ft_tetup(char **tab)
{
	int i;

	while (ft_strcmp(tab[0], "....") == 0)
	{
		i = 0;
		while (i + 1 < 4)
		{
			ft_strcpy(tab[i], tab[i + 1]);
			ft_strcpy(tab[i + 1], "....");
			i++;
		}
	}
	return;
}
